package Dominio;

public class TipoUsuario extends Modelo {
    private int id;
    private String nombre;

    public TipoUsuario(int id) {
        this.setId(id);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }


}

